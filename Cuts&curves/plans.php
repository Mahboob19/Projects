      <center><h3>How to Plan a Successful Dance Class ?</center></h3>
      <p style="margin-right:-10px; font-size:18px;">
This post may contain affiliate links. If you click on one of these links and make a purchase, we may earn a small commission at no additional cost to you. All affiliate links are marked with an asterisk. A friend of mine who teaches high school math was surprised to hear that dance teachers make lesson plans, too. It’s true that lesson planning for school is a lot more complicated, what with national standards and all, but yes, a good dance lesson requires some planning! In this post, I will walk you through my process for planning a dance class. It’s a combination of what I learned in college and my own method that I’ve developed by trial and error.</p>
<h4>Step 1: The Big Picture</h4>
<p style="margin-right:-10px; font-size:18px;">Ideally, you have a list of goals that you’ve been working on throughout the year. (I’m running on the assumption that this is not the first class of the year. That requires a totally different planning method.) Take a look at your goals, what’s been successful, and what needs more work, and use that analysis to create a “focus of the day.”</p>
</div></div>
<div class="row container-fluid" style="margin-left:-15px;">
<div class="col-md-12"style="text-align: justify;"><p style="margin-right:-5px; font-size:18px;">A focus of the day could be pretty much anything you want your students to learn or get better at. It could be something from your list of goals, either something you’ve already worked on that needs more time or something you haven’t gotten to yet. Or, it could be something that’s not on your list of goals that you think is important to cover. Always be open to straying from the master plan if you think your students need something. Let’s pick an example focus of the day to use for the rest of the post. Let’s say we’re teaching a pre-pointe ballet class with girls age 11-12, and we want to work on maintaining turnout in petite allegro.</p>
<h4>Step 2: Plan According to the Focus of the Day</h4><p style="margin-right:-5px; font-size:18px;">
Once you’ve picked a focus of the day, you’re actually going to plan the class backwards. Start by creating a combo that will force the students to work on the focus of the day. In our example, we would make a petite allegro combination, probably with a lot of fifth positions and opportunities for them to check their turnout. Once you have the combo, you play a little game. The goal of the game is that by the time you get to the combo, the students will already have all the knowledge and muscle memory that they need to be successful at it. Here’s how you do that. First, break the combo down into small skills that have to do with your focus of the day. Our list for the petite allegro combo might look like this:
<ul>
<li type="desc"> Closing in turned out fifth position after glissade</li>
<li type="desc">Brushing with a turned out leg in jeté and glissade</li>
<li type="desc">Landing on a turned out leg in jeté</li>
<li type="desc">Executing pas de boureé with tight, turned-out steps</li>
<li type="desc">Sending the knees over toes in plié</li>
</ul>
Now, as you create your warm-up, stretch sequence, center, and across-the-floor combos, make sure all of those mini goals are covered. I would make sure to do some degagés that close in plié fifth position to work on the first point at the barre. You could work on landing on a turned out leg by doing fondus that close in fifth. And of course, you can work on knees-over-toes in every plié throughout the class. This approach gives meaning to each combination and creates a clear connection from the beginning to the end of class. Even if you don’t mention the focus of the day to your students, they’ll be able to tell there’s something specific you want them to work on based on the combinations and your corrections. I recommend telling them the focus of the day, because I’ve observed that students like knowing what the teacher is expecting of them, and they even look forward to knowing what the focus of the day is.</p>
<h4>Step 3: Work Out the Timing</h4><p style=" margin-right:-5px;font-size:18px;">This is the part I always struggle with, figuring out how much time to spend on each exercise. Invariably, I end up cutting things in favor of getting to the final combo. It’s a process that I continue to work out for myself, but I have come up with some helpful tips along the way. Hopefully these will help you too!
<ol>
<li type="1">  It’s always better to have too much planned than not enough.</li>
<li type="1">  Leave at least 10 minutes unplanned. 5 minutes for you giving corrections and 5 minutes to account for things taking longer than expected, the teacher before you running late, etc.</li>
<li type="1">  Don’t forget to take questions into account. You may need to repeat something once or twice if your students aren’t picking it up. But don’t leave time for questions on every combo, or it’ll pad your lesson plan with unfilled time.</li>
<li type="1">  Around recital time, you will inevitably be interrupted by another class wanting to show their recital dance (or your kids asking to). Take that into account or you’ll never get everything done that you want to!</li>
<li type="1">  Make note of what you did and didn’t have time for, so you can revisit it next week.
 </li></ol>
Now you should have a solid lesson plan that will help your class be successful!
