        <p>
          Salsa is a popular form of social dance originating in Eastern Cuba[citation needed]. The Salsa we hear now is said to be born in New York to a mixture of Afro Cuban folk dances with Jazz. Evidence shows that the “Salsa” sound was already developed in Cuba before being brought up to New York[citation needed]. The movements of Salsa are a combination of the Afro-Cuban dances Son, cha-cha-cha, Mambo, Rumba, and the Danzón. The dance, along with salsa music,[1][2][3] saw major development in the mid-1970s in New York.[4] Different regions of Latin America and the United States have distinct salsa styles of their own, such as Cuban, Puerto Rican, Cali Colombia, L.A. and New York styles. Salsa dance socials are commonly held in night clubs, bars, ballrooms, restaurants, and outside, especially when part of an outdoor festival.</p>

          <p>In many styles of salsa dancing, as a dancer shifts their weight by stepping, the upper body remains level and nearly unaffected by the weight changes. Weight shifts cause the hips to move. Arm and shoulder movements are also incorporated. Salsa generally uses music ranging from about 150 bpm (beats per minute) to around 250 bpm, although most dancing is done to music somewhere between 160–220 bpm. The basic Salsa dance rhythm consists of taking three steps for every four beats of music. The odd number of steps creates the syncopation inherent to Salsa dancing and ensures that it takes 8 beats of music to loop back to a new sequence of steps.
